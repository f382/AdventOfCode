my @pairs = (
	['(',')'],
	['[',']'],
	['{','}'],
	['<','>']
);

my %scores = (')' => 3, ']' => 57, '}' => 1197, '>' => 25137);

my %match = ();

foreach my $p (@pairs) {
	$match{$p->[0]} = $p->[1];
}

sub isopen {
	my($c) = @_;
	return defined($match{$c});
}

sub isclose {
	my($c, @stack) = @_;
	return 0 unless @stack;
	return $match{$stack[@stack - 1]} eq $c;
}

sub parse {
	my($line) = @_;
	print "$line\n";
	my @c = split //, $line;
	my @stack = ();
	foreach my $c (@c) {
		if (isopen($c)) {
			push @stack, $c;
			next;
		}
		if (!isclose($c, @stack)) {
			return $c;
		}
		pop @stack;
	}
	return undef;
}

my $score;

while ($input =~ /^(.*$)/mg) {
	my $line = $1;
	my $c = parse($line);
	next unless defined($c);
	$score += $scores{$c};
	print "$c, $score\n";
}
